<p class="font_label">Определение Вашего местоположения на Android</p>
<p class="font_text">Для передачи координат приложению Arenda используется функция геолокации Вашего телефона\планшета. Местоположение определяется на основе 3G, Wi-Fi и GPS. Наиболее высокую точность обеспечивает GPS, но ее можно сделать ещё более высокой, разрешив устройству также использовать 3G и Wi-Fi (убедитесь, что передача данных по Wi-Fi включена).<br><br>Чтобы включить определение местоположения на устройстве :<br><br>
1. На устройстве войдите в <b>Настройки</b>.<br>
2. Нажмите пункт <b>Местоположение</b> или <b>Мое местоположение/Отправка геоданных</b>.<br>
3. Убедитесь, что все галочки включены (например, <b>Доступ к моему местоположению</b>, <b>По спутниками GPS</b>, <b>По Wi-Fi и мобильной сети</b> или <b>Беспроводные сети</b>).<br><br>
Перед запуском Arenda проверьте, есть ли подключение к Интернету (значок 3G или Wi-Fi в углу экрана). Если <b>GPS навигация</b> включена, то в окне Arenda Вы также увидите соответствующий значок :<br><br><img src="gps.png"><br><br>
Если после вышеуказанных действий местоположение не определяется, пожалуйста, нажмите <b>очистить кэш</b> сервисов Google Play (<b>Настройки > Приложения/Диспетчер приложений > Сервисы Google Play</b>), либо напишите нам через приложение :<br><br>
1. Откройте настройки приложения.<br>
2. Выберите пункт <b>Обратная связь</b>.<br><br></p>
